<h1>Frozen's Story</h1>
<br> <br> 
            <p>
                When Princess Elsa and Princess Anna of Arendelle were little girls, they were best of friends.

                  Anna was one of the only people who knew Elsa's secret, Elsa had the power to make snow and ice!
                  One night, Elsa filled an empty ballroom with snow so the sisters could play together.
                  <br> <br>

                   But as they played, Elsa lost the control. She accidently hit Anna with the blast of icy magic. Anna was badly hurt. Her parents went to the ancient mountains trolls for help. A wise old troll told them that Anna could be saved- she was lucky to have been hit in the head, not in the heart.
                  Anna did not remember what had happened.
                  <br> <br>
                   To keep Elsa's gift a secret from everyone else, their parents surrounded the castle with walls and never let anyone inside. But whenever Elsa had strong feelings, the magic still spilled out. Elsa didn't want to hurt her sister again, so she stopped playing with Anna.
                   <br> <br>
                    That made Anna very lonely. Even after their parents were lost in a storm sea, the sisters didn't spend any time together.
                  Years later, it was time for Elsa to become queen of Arendelle. For just that day, the castle gates were opened. Hundreds of people attended the coronation ceremony. Elsa worked hard to hide her feelings and powers.
                  <br> <br>
                   At the party, Anna danced with handsome prince Hans from the Southern Isles. He made her heart flutter. It seemed like they had everything in common. They decided to get married. Elsa thought their engagement was a bad idea. Anna couldn’t believe it and started arguing. Elsa started to lose control and as she shouted, ice shot from her hands. Everyone stared at Elsa in shock. Now all the Arendelle knew Elsa’s secret.
                   <br> <br>
                    She panicked and fled for the mountains.
                  Anna felt horrible. Elsa's powers had created a terrible winter storm- in the middle of summer. She left Hans in charge of the kingdom and raced after Elsa. As Anna rode through the fierce wind, her horse threw her into the snow and ran off back to Arendelle. Luckily, she met an ice harvester named Kristoff and his reindeer, Sven.
                  <br> <br>
                   Together they set off to look for Elsa. As they climbed the mountain, Anna and Kristoff discovered a beautiful winter wonderland. They met an enchanted snowman named Olaf. Olaf knew where Elsa was and wanted to help them to bring back summer. Meanwhile, Hans was helping the people of Arendelle. When Anna's horse came back to the castle without her, Hans turned to the crowd and asked for volunteers. Back on the mountain, Olaf led Anna and Kristoff to a giant ice palace that Elsa had created with her powers. Even Kristoff was impressed.
                   <br> <br>
                    Inside, Anna told Elsa about the terrible storm in Adrendelle. Elsa was worried and didn't know how to unfreeze the town, and she thought that Anna and Arendelle might be better off without her.
                  Anna wanted Elsa to come home but Elsa was too scared that she would hurt more people. As the sisters argued, an icy wave of magic burst from Elsa's body and struck Anna in the chest. Anna wouldn't leave without her sister.
                  <br> <br>
                   Elsa knew what she had to do. She used her magic to create a giant snowman to chase Anna and her friends out of the palace. The friends landed safely below. They had escaped from the snowman but Anna was worried as her hair was turning snowy white. Kristoff brought Anna to the trolls. A troll told them that unless Elsa's magic was reversed, Anna would be frozen solid. Only an act of true love could thaw a frozen heart.
                   <br> <br>
                    Anna knew she loved Hans. Anna began to shiver. Kristoff was worried about her. Hans and his soldiers had just arrived at the ice palace and attacked Elsa. They brought her back to Arendelle and threw her in the dungeon. When Anna arrived in Arendelle, she asked Hans to save her with a kiss, he refused as he had only pretended to love her. He wanted to take over Arendelle by getting rid of Anna and Elsa.
                    <br> <br>
                     Hans left Anna alone and shivering. Olaf found her and helped her warm up but Anna was getting weaker. Olaf glanced out of the window and saw Kristoff racing toward the castle. Kristoff was the one Anna needed to kiss. Elsa had escaped from the dungeon but Hans was close behind her. Hans told Elsa that her magic blast hit Anna's, heart.
                     <br> <br>
                      Elsa collapsed in the snow and closed her eyes. Everything she had done to protect her sister had failed.
                  Nearby, Anna hurried towards Kristoff when he heard Hans draw his sword. Instead of saving herself, Anna leaped in front of her sister. Hans swung his sword but it shattered against Anna's frozen body. Elsa clutched her sister.
                  <br> <br>
                   Suddenly, Anna began to thaw and the two sisters hugged. As Olaf watched them, he remembered what the wise old troll had said: "An act of true love will thaw a frozen heart." Anna's love for Elsa had saved both of them and the Kingdom. The two sisters were best friends again and summer had returned to Arendelle. Elsa even made Olaf a little snow cloud to keep him from melting. Elsa had a surprise for Anna- the castle gates were wide open.







